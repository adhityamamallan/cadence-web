#!/usr/bin/env node
// Fetches the publish age of each "new" package version listed in
// CHANGESET-LOCKFILE.md by querying the npm registry.
//
// Usage:
//   node scripts/fetch-version-ages.mjs [path-to-changeset-md]
//
// Output: a table of package | version | published | age, sorted oldest-first.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const mdPath = resolve(
  process.argv[2] ?? resolve(__dirname, '..', 'CHANGESET-LOCKFILE.md')
);

const REGISTRY = 'https://registry.npmjs.org';
const CONCURRENCY = 10;

/**
 * Parse the markdown tables. We want the resulting ("new") version:
 *  - "Changed" section: 3 columns, take the last (After).
 *  - "Added" section:   2 columns, take the last (Version).
 *  - "Removed" section: skipped (package no longer present).
 */
function parseChangeset(md) {
  const lines = md.split('\n');
  const pkgs = [];
  let section = null;
  for (const line of lines) {
    const header = line.match(/^##\s+(\w+)/);
    if (header) {
      section = header[1].toLowerCase();
      continue;
    }
    if (section === 'removed') continue;
    const row = line.match(/^\|\s*`([^`]+)`\s*\|(.+)\|/);
    if (!row) continue;
    const name = row[1];
    const cells = row[2].split('|').map((c) => c.trim());
    const version = cells[cells.length - 1]; // last column = new version
    if (version) pkgs.push({ name, version });
  }
  // de-dupe by name@version
  const seen = new Set();
  return pkgs.filter(({ name, version }) => {
    const key = `${name}@${version}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function encodeName(name) {
  // scoped packages: @scope/pkg -> @scope%2Fpkg
  return name.replace('/', '%2F');
}

function humanizeAge(fromIso, now) {
  const then = new Date(fromIso).getTime();
  const days = Math.floor((now - then) / 86_400_000);
  if (days < 0) return 'in the future?';
  if (days === 0) return 'today';
  const years = Math.floor(days / 365);
  const months = Math.floor((days % 365) / 30);
  if (years > 0) return `${years}y ${months}mo`;
  if (months > 0) return `${months}mo`;
  return `${days}d`;
}

const MAX_RETRIES = 3;

async function fetchPublishTime({ name, version }) {
  let lastErr;
  for (let attempt = 1; attempt <= MAX_RETRIES; attempt++) {
    try {
      const res = await fetch(`${REGISTRY}/${encodeName(name)}`);
      if (!res.ok) {
        return { name, version, error: `HTTP ${res.status}` };
      }
      const data = await res.json();
      const published = data.time?.[version];
      if (!published) {
        return { name, version, error: 'version not in registry time map' };
      }
      return { name, version, published };
    } catch (e) {
      lastErr = e;
      await new Promise((r) => setTimeout(r, 250 * attempt));
    }
  }
  return { name, version, error: lastErr?.message ?? 'fetch failed' };
}

async function runPool(items, worker, concurrency) {
  const results = new Array(items.length);
  let i = 0;
  const runners = Array.from({ length: concurrency }, async () => {
    while (i < items.length) {
      const idx = i++;
      results[idx] = await worker(items[idx]);
    }
  });
  await Promise.all(runners);
  return results;
}

async function main() {
  const md = readFileSync(mdPath, 'utf8');
  const pkgs = parseChangeset(md);
  process.stderr.write(
    `Fetching publish dates for ${pkgs.length} versions from npm...\n`
  );

  const now = Date.now();
  const results = await runPool(pkgs, fetchPublishTime, CONCURRENCY);

  const ok = results.filter((r) => r.published);
  const failed = results.filter((r) => r.error);

  ok.sort((a, b) => new Date(a.published) - new Date(b.published));

  const nameW = Math.max(7, ...ok.map((r) => r.name.length));
  const verW = Math.max(7, ...ok.map((r) => r.version.length));

  const pad = (s, w) => s.padEnd(w);
  console.log(
    `${pad('Package', nameW)}  ${pad('Version', verW)}  ${pad('Published', 10)}  Age`
  );
  console.log('-'.repeat(nameW + verW + 30));
  for (const r of ok) {
    const date = r.published.slice(0, 10);
    console.log(
      `${pad(r.name, nameW)}  ${pad(r.version, verW)}  ${pad(date, 10)}  ${humanizeAge(r.published, now)}`
    );
  }

  if (failed.length) {
    console.log(`\n${failed.length} could not be resolved:`);
    for (const r of failed) {
      console.log(`  ${r.name}@${r.version} — ${r.error}`);
    }
  }

  writeMarkdown(ok, failed, now);
}

function writeMarkdown(ok, failed, now) {
  const outPath = resolve(__dirname, '..', 'CHANGESET-VERSION-AGES.md');
  let md = '# New version ages: v4.0.16-rc.02 -> v4.0.16-rc.03\n\n';
  md += `Publish dates from the npm registry, generated by \`scripts/fetch-version-ages.mjs\` on ${new Date(now).toISOString().slice(0, 10)}. Sorted oldest-first.\n\n`;
  md += `## Resolved (${ok.length})\n\n`;
  md += '| Package | Version | Published | Age |\n|---|---|---|---|\n';
  for (const r of ok) {
    md += `| \`${r.name}\` | ${r.version} | ${r.published.slice(0, 10)} | ${humanizeAge(r.published, now)} |\n`;
  }
  if (failed.length) {
    md += `\n## Unresolved (${failed.length})\n\n`;
    md += '| Package | Version | Error |\n|---|---|---|\n';
    for (const r of failed) {
      md += `| \`${r.name}\` | ${r.version} | ${r.error} |\n`;
    }
  }
  writeFileSync(outPath, md);
  process.stderr.write(`Wrote ${outPath}\n`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
